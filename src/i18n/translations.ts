export const translations: Record<LangCode, T> = { fr, en, zh, de, es, ko, pt, it, nl, ar, ja, da }

export function detectLang(): LangCode {
  if (typeof window === 'undefined') return 'en'
  const saved = localStorage.getItem('voltpilot_lang') as LangCode | null
  if (saved && saved in translations) return saved
  const browser = navigator.language.split('-')[0].toLowerCase()
  if (browser in translations) return browser as LangCode
  return 'en'
}
